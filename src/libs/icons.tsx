import React from "react";

type IconProperties = {
  color: string;
  width: number;
  height: number;
  className?: string;
};

type IconWrapperProperties = {
  color: string;
  hoverColor?: string;
  width: number;
  height: number;
  className?: string;
  onClick?: React.MouseEventHandler<HTMLDivElement>;
};

type IconWrapperFuncProperties = {
  (children: (e: IconProperties) => JSX.Element, props: IconWrapperProperties);
};

export const IconWrapper: IconWrapperFuncProperties = (
  children,
  { color, hoverColor, className, width, height, onClick }
) => {
  const [isHover, setIsHover] = React.useState(false);
  const iconColor = isHover ? hoverColor : color;
  return (
    <div
      onMouseEnter={() => setIsHover(true)}
      onMouseLeave={() => setIsHover(false)}
      onClick={onClick}
    >
      {children({
        color: iconColor,
        className,
        width,
        height,
      })}
    </div>
  );
};

